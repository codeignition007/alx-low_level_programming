#include "lists.h"

/**
 * add_nodeint - At the beginning of a linked list new nodes is added
 * @head: First node in the list pointer
 * @n: An inserted data in the new node
 *
 * Return: pointer to the new node, or NULL if it fails
 */
listint_t *add_nodeint(listint_t **head, const int n)
{
	listint_t *new;

	new = malloc(sizeof(listint_t));
	if (!new)
		return (NULL);

	new->n = n;
	new->next = *head;
	*head = new;

	return (new);
}
